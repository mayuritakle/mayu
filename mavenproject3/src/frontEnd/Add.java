    private void addsubmitbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addsubmitbtnActionPerformed
        // TODO add your handling code here:
        AddRequest ar = new AddRequest();

        String[] custAdd = {addreqnametf1.getText(), addmobtf2.getText(), addbiketf3.getText(),
                            addbikenumtf4.getText(), adddatetf5.getText()};

        int x = ar.insertAddServicesTbl(custAdd);
        if (x == 0) {
            JOptionPane.showMessageDialog(null, "Error. try again!!");
        } else if (x == 1) {
            JOptionPane.showMessageDialog(null, "Request Added");
            User ds = new User();
            ds.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_addsubmitbtnActionPerformed

